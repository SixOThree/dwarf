using Avalonia;
using Avalonia.Controls.ApplicationLifetimes;
using Avalonia.Markup.Xaml;

namespace Dwarf.UI.Avalonia;

// Avalonia Application root. Creates the MainWindow on startup.
public class App : Application
{
    public override void Initialize() => AvaloniaXamlLoader.Load(this);

    public override void OnFrameworkInitializationCompleted()
    {
        if (ApplicationLifetime is IClassicDesktopStyleApplicationLifetime desktop)
        {
            desktop.MainWindow = new MainWindow();
        }
        base.OnFrameworkInitializationCompleted();
    }

    // Standard Avalonia bootstrap entry. Called by the host process
    // (Dwarf.Cli) which then chains `.UsePlatformDetect().LogToTrace()`
    // (those extensions live in `Avalonia.Desktop` which only the host
    // process references — the library project stays platform-agnostic).
    public static AppBuilder BuildAvaloniaApp() =>
        AppBuilder.Configure<App>();
}
